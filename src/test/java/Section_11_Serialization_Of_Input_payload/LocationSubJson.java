package Section_11_Serialization_Of_Input_payload;

public class LocationSubJson {
	private double lat;
	private double lng;

// 1. Lat
	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}

// 2. Lng
	public double getLng() {
		return lng;
	}

	public void setLng(double lng) {
		this.lng = lng;
	}
}
